        {
            string[] lines = value.Split("\r\n");

            int width = lines[0].Length;
            int height = lines.Length;
            var data = new char[width, height];

            for (int x = 0; x < width; x++)
            {
                for (int y = 0; y < height; y++)
                {
                    data[x, y] = lines[y][x];
                }
            }

            return data;
        }
